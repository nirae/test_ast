#include "libft.h"

int		ft_lstlen(t_list *lst)
{
	int		i;
	t_list	*tmp;

	if (lst == NULL)
		return (0);
	i = 0;
	tmp = lst;
	while (tmp != NULL)
	{
		tmp = tmp->next;
		i++;
	}
	return (i);
}
