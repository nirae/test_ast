int		ft_sqrt(int n)
{
	int i;

	i = 0;
	if (n <= 0)
		return (0);
	if (n > 2147395600)
		return (0);
	while (i * i < n)
		i++;
	if (n == 0)
		return (0);
	return (i);
}
